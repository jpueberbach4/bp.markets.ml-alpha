def set_seed(seed):
    """
    Enforces deterministic execution across all random number generators.
    This guarantees that the PyTorch weight initialization and data shuffling 
    remain identical across distinct training runs for accurate A/B testing.
    """
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)

    if torch.cuda.is_available():
        torch.cuda.manual_seed(seed)
        torch.cuda.manual_seed_all(seed)

    # Disable cuDNN benchmarking to ensure perfectly reproducible CUDA kernel selection
    torch.backends.cudnn.enabled = True
    torch.backends.cudnn.benchmark = False
    torch.backends.cudnn.deterministic = True

