def get_expert_config():
    """
    Constructs the final, immutable ExpertConfig object to be ingested by the PyTorch model.
    It recursively calculates the exact `input_dim` for each squad based on the length 
    of its configured feature list.
    """
    from lib.experts.config import ExpertConfig, TrainingConfig

    def _set_input_dims(squads_dict):
        for k, v in squads_dict.items():
            v.input_dim = len(v.features)
            if v.children:
                _set_input_dims(v.children)

    _set_input_dims(SQUADS)

    return ExpertConfig(
        expert_id="Feature_Routed_MoE",
        model_dim=MODEL_DIM,  
        squads=SQUADS,
        training=TrainingConfig(
            # Hyperparameters derived from the latest Optuna optimization trial
            lr=0.00038713547639704615,
            weight_decay=0.01422659477874579,
            decision_threshold=0.5,
            pos_weight=1.6358219756228292,
            aux_loss_coef=0.07104187870164635
        )
    )

